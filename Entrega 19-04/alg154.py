placa = input('Entre com a placa do carro: ')
ult = placa[-1]
if ult == '1':
    print('Janeiro')
elif ult == '2':
    print('Fevereiro')
elif ult == '3':
    print('Março')
elif ult == '4':
    print('Abril')
elif ult == '5':
    print('Maio')
elif ult == '6':
    print('Junho')
elif ult == '7':
    print('Julho')
elif ult == '8':
    print('Agosto')
elif ult == '9':
    print('Setembro')
elif ult == '0':
    print('Outubro')
else:
    print('Opção inválida')